package com.sgtesting.corejavacasting;

public class ImplicitCasting {

	public static void main(String[] args) {
		byte num1 = 10;
		short num2 = num1;
		System.out.println(num2);
		
		int num3 = 100;
		long num4 = num3;
		System.out.println(num4);
		
		char ch = 'A';
		int num5 = ch;
		System.out.println(num5);
	}
}

