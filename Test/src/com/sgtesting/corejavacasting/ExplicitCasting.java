package com.sgtesting.corejavacasting;

public class ExplicitCasting {
	public static void main(String[] args) {

		short num1 = 100;
		byte num2 = (byte) num1;
		System.out.println(num2);

		long num3 = 1000;
		int num4 = (int) num3;
		System.out.println(num4);


		int num5 = 66;
		char ch = (char) num5;
		System.out.println(ch);
	}
}

